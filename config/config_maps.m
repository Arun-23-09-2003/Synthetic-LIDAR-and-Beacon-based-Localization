function scenario = make_scenario(id, name, map_type, bounds, duration_s, seed, ...
    beacon_positions, beacon_max_ranges, beacon_penetration_budgets, waypoints, dropout_scale)
scenario.id = id;
scenario.name = name;
scenario.map_type = map_type;
scenario.bounds = bounds;
scenario.duration_s = duration_s;
scenario.seed = seed;
scenario.beacon_positions = beacon_positions';
scenario.beacon_max_ranges = beacon_max_ranges(:)';
scenario.beacon_penetration_budgets = beacon_penetration_budgets(:)';
scenario.trajectory_waypoints = waypoints';
scenario.dropout_scale = dropout_scale;
end
